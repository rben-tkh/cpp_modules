#include <iostream>

/*-----------------------------------T_SWAP----------------------------------*/

template <typename T>
void	swap(T &a, T &b)
{
	T	temp = a;
	a = b;
	b = temp;
}

/*-----------------------------------T_MIN-----------------------------------*/

template <typename T>
T	min(const T &a, const T &b)
{
	if (a < b)
		return (a);
	return (b);
}

/*-----------------------------------T_MAX-----------------------------------*/

template <typename T>
T	max(const T &a, const T &b)
{
	if (a > b)
		return (a);
	return (b);
}

